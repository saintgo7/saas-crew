import type { Course, Chapter, CourseDetailResponse } from '@/lib/api/types'

// Generic chapter templates used to generate demo chapters per course
const CHAPTER_TEMPLATES = [
  { suffix: '소개 및 환경 설정', desc: '학습 환경을 구성하고 핵심 개념을 소개합니다.' },
  { suffix: '기본 개념 이해', desc: '기초 이론과 핵심 원리를 학습합니다.' },
  { suffix: '핵심 기능 실습', desc: '핵심 기능을 직접 실습하며 익힙니다.' },
  { suffix: '패턴과 모범 사례', desc: '실무에서 자주 사용되는 패턴과 모범 사례를 학습합니다.' },
  { suffix: '중급 활용법', desc: '더 깊이 있는 활용 방법을 학습합니다.' },
  { suffix: '고급 기법', desc: '고급 기법과 최적화 방법을 다룹니다.' },
  { suffix: '실전 프로젝트', desc: '배운 내용을 활용하여 실전 프로젝트를 진행합니다.' },
  { suffix: '마무리 및 다음 단계', desc: '전체 내용을 정리하고 심화 학습 방향을 제시합니다.' },
]

function generateDemoChapters(course: Course): Chapter[] {
  const count = Math.min(course.chaptersCount || 5, 8)
  const avgDuration = Math.max(Math.floor((course.duration || 600) / count), 15)
  const mainTag = course.tags?.[0] || course.title.split(' ')[0]

  return Array.from({ length: count }, (_, i) => {
    const template = CHAPTER_TEMPLATES[i % CHAPTER_TEMPLATES.length]
    const title = `${mainTag} ${template.suffix}`

    return {
      id: `${course.id}-ch-${i + 1}`,
      courseId: course.id,
      title,
      description: template.desc,
      orderIndex: i + 1,
      duration: avgDuration,
      content: [
        `# ${title}`,
        '',
        template.desc,
        '',
        '## 학습 목표',
        `- ${mainTag}의 ${template.suffix.replace(' 및 ', '과 ')} 이해`,
        '- 실전 예제를 통한 연습',
        '- 프로젝트에 적용하는 방법 습득',
        '',
        '## 주요 내용',
        `이 챕터에서는 **${course.title}** 과정의 핵심 내용 중 하나인 "${template.suffix}"에 대해 다룹니다.`,
        '',
        '> 데모 콘텐츠입니다. 실제 과정에서는 더 자세한 내용이 제공됩니다.',
      ].join('\n'),
      createdAt: course.createdAt,
      updatedAt: course.updatedAt,
    }
  })
}

export function getDemoCourseDetail(courseId: string): CourseDetailResponse | null {
  const course = DEMO_COURSES.find((c) => c.id === courseId)
  if (!course) return null

  return {
    course: {
      ...course,
      chapters: generateDemoChapters(course),
    },
    enrollment: undefined,
    isEnrolled: false,
  }
}

